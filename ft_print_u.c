#include "ft_printf.h"

void	ft_put_unsigned(unsigned int n, int *p)
{
	if (n >= 0 && n <= 9)
	{
		ft_putchar(n + '0', p);
	}
	if (n >= 10)
	{
		ft_putnbr(n / 10, p);
		ft_putchar((n % 10) + '0', p);
	}
}
